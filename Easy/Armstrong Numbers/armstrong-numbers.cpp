class Solution {
  public:
    string armstrongNumber(int n){
        // code here
        int c = n;
        int sum = 0;
        while(c > 0){
            int temp = c%10;
            sum += (temp*temp*temp);
            c = c/10;
        }
        if(sum == n){
            return "Yes";
        }
        return "No";
    }
};

